#!/usr/bin/env python3
"""
Result Aggregator - 结果聚合器
负责聚合和格式化代理执行结果
"""

import time
from typing import Dict, Any, List


class ResultAggregator:
    """结果聚合器"""

    def __init__(self):
        """初始化结果聚合器"""
        pass

    def aggregate_results(self, agent_results: Dict[str, Any], workflow_def: Dict[str, Any]) -> str:
        """
        聚合代理执行结果

        Args:
            agent_results: 代理执行结果
            workflow_def: 工作流定义

        Returns:
            聚合后的文档内容
        """
        # 提取各个代理的结果
        code_analysis = self._extract_agent_result(agent_results, 'code-analyst')
        architecture_analysis = self._extract_agent_result(agent_results, 'architecture-analyst')
        flow_analysis = self._extract_agent_result(agent_results, 'flow-analyst')
        problem_diagnosis = self._extract_agent_result(agent_results, 'problem-solver')
        documentation = self._extract_agent_result(agent_results, 'doc-engineer')

        # 生成最终文档
        final_document = self._generate_final_document(
            code_analysis,
            architecture_analysis,
            flow_analysis,
            problem_diagnosis,
            documentation
        )

        return final_document

    def _extract_agent_result(self, agent_results: Dict[str, Any], agent_id: str) -> Dict[str, Any]:
        """
        提取指定代理的结果

        Args:
            agent_results: 代理执行结果
            agent_id: 代理ID

        Returns:
            代理结果
        """
        for task_id, result in agent_results.items():
            if result.get('agent_id') == agent_id:
                return result.get('result', {})
        return {}

    def _generate_final_document(self, code_analysis: Dict, architecture_analysis: Dict,
                                flow_analysis: Dict, problem_diagnosis: Dict,
                                documentation: Dict) -> str:
        """
        生成最终文档

        Args:
            code_analysis: 代码分析结果
            architecture_analysis: 架构分析结果
            flow_analysis: 流程分析结果
            problem_diagnosis: 问题诊断结果
            documentation: 文档生成结果

        Returns:
            最终文档内容
        """
        doc_parts = []

        # 文档标题
        doc_parts.append("# Technical Documentation Generated by BMAD Agents")
        doc_parts.append(f"\n**Generated at:** {time.strftime('%Y-%m-%d %H:%M:%S')}")
        doc_parts.append("**Agents used:** code-analyst, architecture-analyst, flow-analyst, problem-solver, doc-engineer")
        doc_parts.append("\n---\n")

        # 代码分析部分
        if code_analysis:
            doc_parts.append("## Code Analysis")
            doc_parts.append("\n### Codebase Structure")
            if 'codebase_structure' in code_analysis:
                structure = code_analysis['codebase_structure']
                doc_parts.append(f"- Total Files: {structure.get('total_files', 'N/A')}")
                doc_parts.append(f"- Languages: {', '.join(structure.get('languages', []))}")
                doc_parts.append(f"- Main Directories: {', '.join(structure.get('main_directories', []))}")
                doc_parts.append(f"- Complexity Score: {structure.get('complexity_score', 'N/A')}")

            doc_parts.append("\n### Dependencies")
            if 'dependencies' in code_analysis:
                deps = code_analysis['dependencies']
                for lang, packages in deps.items():
                    doc_parts.append(f"- **{lang.title()}:** {', '.join(packages)}")

            doc_parts.append("\n### Code Quality")
            if 'code_quality' in code_analysis:
                quality = code_analysis['code_quality']
                doc_parts.append(f"- Maintainability: {quality.get('maintainability', 'N/A')}")
                doc_parts.append(f"- Test Coverage: {quality.get('test_coverage', 'N/A')}")
                doc_parts.append(f"- Documentation: {quality.get('documentation', 'N/A')}")

        # 架构分析部分
        if architecture_analysis:
            doc_parts.append("\n## Architecture Analysis")

            if 'architecture_patterns' in architecture_analysis:
                doc_parts.append("\n### Architecture Patterns")
                patterns = architecture_analysis['architecture_patterns']
                for pattern in patterns:
                    doc_parts.append(f"- {pattern}")

            if 'component_diagram' in architecture_analysis:
                doc_parts.append("\n### Component Diagram")
                components = architecture_analysis['component_diagram']
                doc_parts.append("#### Components:")
                for comp in components.get('components', []):
                    doc_parts.append(f"- {comp['name']} ({comp['type']})")

                doc_parts.append("\n#### Relationships:")
                for rel in components.get('relationships', []):
                    doc_parts.append(f"- {rel['from']} → {rel['to']} ({rel['type']})")

            if 'deployment_architecture' in architecture_analysis:
                doc_parts.append("\n### Deployment Architecture")
                deployment = architecture_analysis['deployment_architecture']
                doc_parts.append(f"- Tiers: {' → '.join(deployment.get('tiers', []))}")
                doc_parts.append(f"- Technologies: {', '.join(deployment.get('technologies', []))}")

        # 流程分析部分
        if flow_analysis:
            doc_parts.append("\n## Flow Analysis")

            if 'business_flows' in flow_analysis:
                doc_parts.append("\n### Business Flows")
                flows = flow_analysis['business_flows']
                for flow in flows:
                    doc_parts.append(f"\n#### {flow['name']}")
                    doc_parts.append(f"- Complexity: {flow['complexity']}")
                    doc_parts.append("- Steps:")
                    for step in flow['steps']:
                        doc_parts.append(f"  - {step}")

            if 'data_flows' in flow_analysis:
                doc_parts.append("\n### Data Flows")
                data_flows = flow_analysis['data_flows']
                for flow in data_flows:
                    doc_parts.append(f"\n#### {flow['name']}")
                    doc_parts.append(f"- Source: {flow['source']}")
                    doc_parts.append(f"- Destination: {flow['destination']}")
                    doc_parts.append(f"- Transformation: {flow['transformation']}")

        # 问题诊断部分
        if problem_diagnosis:
            doc_parts.append("\n## Problem Diagnosis")

            if 'identified_problems' in problem_diagnosis:
                doc_parts.append("\n### Identified Problems")
                problems = problem_diagnosis['identified_problems']
                for problem in problems:
                    doc_parts.append(f"\n#### {problem['category']} - {problem['severity']}")
                    doc_parts.append(f"- **Description:** {problem['description']}")
                    doc_parts.append(f"- **Impact:** {problem['impact']}")
                    doc_parts.append(f"- **Recommendation:** {problem['recommendation']}")

            if 'risk_assessment' in problem_diagnosis:
                doc_parts.append("\n### Risk Assessment")
                risks = problem_diagnosis['risk_assessment']
                doc_parts.append(f"- Overall Risk: {risks.get('overall_risk', 'N/A')}")
                doc_parts.append(f"- Security Risk: {risks.get('security_risk', 'N/A')}")
                doc_parts.append(f"- Performance Risk: {risks.get('performance_risk', 'N/A')}")
                doc_parts.append(f"- Maintainability Risk: {risks.get('maintainability_risk', 'N/A')}")

        # 文档生成部分
        if documentation:
            doc_parts.append("\n## Generated Documentation")

            # 流程分析文档
            if 'flow_analysis_doc' in documentation:
                doc_parts.append("\n### Flow Analysis Documentation")
                doc_parts.append(documentation['flow_analysis_doc'].get('content', ''))

            # 问题诊断文档
            if 'problem_diagnosis_doc' in documentation:
                doc_parts.append("\n### Problem Diagnosis Documentation")
                doc_parts.append(documentation['problem_diagnosis_doc'].get('content', ''))

            # 质量验证结果
            if 'quality_validation' in documentation:
                doc_parts.append("\n### Quality Validation")
                validation = documentation['quality_validation']
                doc_parts.append(f"- Quality Score: {validation.get('quality_score', 'N/A')}")
                doc_parts.append(f"- Completeness: {validation.get('completeness', 'N/A')}")
                doc_parts.append(f"- Accuracy: {validation.get('accuracy', 'N/A')}")
                doc_parts.append(f"- Readability: {validation.get('readability', 'N/A')}")

                if validation.get('issues_found'):
                    doc_parts.append("\n#### Issues Found:")
                    for issue in validation['issues_found']:
                        doc_parts.append(f"- {issue}")

                if validation.get('recommendations'):
                    doc_parts.append("\n#### Recommendations:")
                    for rec in validation['recommendations']:
                        doc_parts.append(f"- {rec}")

        # 总结
        doc_parts.append("\n## Summary")
        doc_parts.append("\nThis technical documentation has been generated by the BMAD (Business Model Architecture Documentation) agent system.")
        doc_parts.append("The documentation covers code analysis, architecture patterns, business flows, problem diagnosis, and comprehensive technical overview.")
        doc_parts.append("\nFor questions or improvements, please refer to the individual agent outputs and recommendations.")

        return "\n".join(doc_parts)






